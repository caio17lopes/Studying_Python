#Acessando elementos de uma lista
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[0].title()) # utilizando a forma title fica masi elegante e correto a forma que o usuario vê 

# As posições dos indices começam empre pelo zero (0)
print(bicycles[1].title())
print(bicycles[3].title())
# Utilizando o [-1] nós acessamos o ultimo elemento da lista pois ele vai contar da direita para esquerda ex:
print(bicycles[-1].title()) # no caso o ultimo elemento é 'specialized'



