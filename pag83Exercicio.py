# exercicios de aplicação de sorted, reverse, sort 

travel = ['japan','italy','coreia','jericoacoara','arraial do cabo']
print(travel)
print('My travel list:', sorted(travel))
print(travel)
print('My travel list inverted:', sorted(travel, reverse=True))
print(travel)
travel.reverse()
print(travel)
travel.reverse()
print(travel)
travel.sort()
print(travel)
travel.sort(reverse=True)
print(travel)