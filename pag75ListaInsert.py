# usar insert() pode adicionar qualquer elemento na posição que quiser

motocycles = ['honda', 'yamaha', 'suzuki']
motocycles.insert(0,'ducati')
print(motocycles)

# removendo um intem da lista usando 'del'

motocycles = ['honda', 'yamaha', 'suzuki']
del motocycles [0]
print(motocycles)

